package com.vess.model;

public class College {
	 
	
    
	   private int college_id;
	   private String college_city_lvl1;
	   private String college_city_lvl2;
	   private String college_title;
	   private String college_type;
	   private String college_grade;
	    
	   public int getCollegeId() {
	       return college_id;
	   }
	 
	   public void setCollegeId(int college_id) {
	       this.college_id = college_id;
	   }
	 
	   public String getCollegeCityLvl1() {
	       return college_city_lvl1;
	   }
	 
	   public void setCollegeCityLvl1(String college_city_lvl1) {
	       this.college_city_lvl1 = college_city_lvl1;
	   }
	   
	   public String getCollegeCityLvl2() {
	       return college_city_lvl2;
	   }
	 
	   public void setCollegeCityLvl2(String college_city_lvl2) {
	       this.college_city_lvl2 = college_city_lvl2;
	   }
	   
	   public String getCollege_title() {
	       return college_title;
	   }
	 
	   public void setCollege_title(String college_title) {
	       this.college_title = college_title;
	   }
	 
	   public String getCollegeType() {
	       return college_type;
	   }
	 
	   public void setCollegeType(String college_type) {
	       this.college_type = college_type;
	   }
	   
	   
	   
	   public String getCollegeGrade() {
	       return college_grade;
	   }
	 
	   public void setCollegeGrade(String college_grade) {
	       this.college_grade = college_grade;
	   }
	 
	}
